pub fn select(theme: &ColorfulTheme) -> String {
    let options = get_options();
    let selected = dialoguer::FuzzySelect::with_theme(theme)
        .with_prompt("Select an emoji")
        .items(&options)
        .default(0)
        .max_length(12)
        .interact()
        .unwrap();

    get_value(selected)
}
